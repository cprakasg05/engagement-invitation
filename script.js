function setLanguage(lang) {
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // Special logic for story: show only the correct story block
  const storyKeys = ['couple-story-en', 'couple-story-ta', 'couple-story-ml'];
  storyKeys.forEach(key => {
    document.querySelectorAll(`[data-key="${key}"]`).forEach(el => {
      if ((lang === 'en' && key === 'couple-story-en') ||
          (lang === 'ta' && key === 'couple-story-ta') ||
          (lang === 'ml' && key === 'couple-story-ml')) {
        el.style.display = 'block';
      } else {
        el.style.display = 'none';
      }
    });
  });
}

// Event Management & Navigation
let currentEvent = 'marriage';
let storyVisible = false;

function selectEvent(event) {
  currentEvent = event;
  
  // Hide story container if visible
  if (storyVisible) {
    toggleStory();
  }
  
  // Update active button
  document.querySelectorAll('.big-event-btn').forEach(btn => btn.classList.remove('active'));
  document.querySelector(`[onclick="selectEvent('${event}')"]`).classList.add('active');
  
  // Hide home section, show event tabs section
  document.getElementById('home-section').classList.add('hidden');
  document.getElementById('event-tabs-section').classList.remove('hidden');
  
  // Hide all event content wrappers
  document.querySelectorAll('.event-content-wrapper').forEach(wrapper => wrapper.classList.add('hidden'));
  
  // Show active event content
  document.getElementById(`${event}-content`).classList.remove('hidden');
  
  // Reset to venue tab
  switchEventTab('venue');
  
  // Update countdown
  updateCountdown();
}

function switchEventTab(tabName) {
  // Update tab button active state
  document.querySelectorAll('.event-content-tab').forEach(btn => btn.classList.remove('active'));
  document.querySelector(`[onclick="switchEventTab('${tabName}')"]`).classList.add('active');
  
  // Hide all content in both events
  document.querySelectorAll('.event-content').forEach(content => content.classList.add('hidden'));
  
  // Show active tab for current event
  const contentId = `${currentEvent}-${tabName}-content`;
  const activeContent = document.getElementById(contentId);
  if (activeContent) {
    activeContent.classList.remove('hidden');
  }
}

function toggleStory() {
  storyVisible = !storyVisible;
  const storyContainer = document.getElementById('couple-story-container');
  
  if (storyVisible) {
    storyContainer.classList.remove('hidden');
  } else {
    storyContainer.classList.add('hidden');
  }
}

function goBackHome() {
  document.getElementById('home-section').classList.remove('hidden');
  document.getElementById('event-tabs-section').classList.add('hidden');
  toggleStory(); // Close story if open
}

document.addEventListener('DOMContentLoaded', () => {
  // Load saved language preference
  let lang = localStorage.getItem("siteLang") || "en";
  setLanguage(lang);

  // Audio Control
  const audio = document.querySelector('audio');
  const muteButton = document.querySelector('#mute-btn') || document.querySelector('#muteButton');
  
  if (muteButton && audio) {
    muteButton.addEventListener('click', () => {
      audio.muted = !audio.muted;
      muteButton.innerHTML = audio.muted ? '&#128263;' : '&#128266;';
    });
  }

  // Countdown Timer
  const countdownEl = document.getElementById('countdown-timer') || document.getElementById('countdown');
  if (countdownEl) {
    updateCountdown();
    setInterval(updateCountdown, 1000);
  }
});

function updateCountdown() {
  const countdownEl = document.getElementById('countdown-timer') || document.getElementById('countdown');
  if (!countdownEl) return;
  
  // Marriage: 1st February 2026, Reception: 8th February 2026
  const marriageDate = new Date('2026-02-01T06:00:00+05:30');
  const receptionDate = new Date('2026-02-08T06:00:00+05:30');
  const now = new Date();
  
  // Determine which event to count down to
  let eventDate = currentEvent === 'marriage' ? marriageDate : receptionDate;
  let eventLabel = currentEvent === 'marriage' ? 'Marriage' : 'Reception';
  
  // If past marriage, count to reception; if past both, show message
  if (now > marriageDate && now > receptionDate) {
    countdownEl.innerHTML = '<span class="countdown-live">Thank you for celebrating with us! тЭдя╕П</span>';
    return;
  } else if (now > marriageDate) {
    eventDate = receptionDate;
    eventLabel = 'Reception';
  }
  
  const diff = eventDate - now;
  if (diff > 0) {
    const days = Math.floor(diff / (1000 * 60 * 60 * 24));
    const hours = Math.floor((diff / (1000 * 60 * 60)) % 24);
    const minutes = Math.floor((diff / (1000 * 60)) % 60);
    const seconds = Math.floor((diff / 1000) % 60);
    countdownEl.innerHTML = `<span class="countdown-label">${eventLabel} starts in:</span> <span class="countdown-numbers">${days}d ${hours}h ${minutes}m ${seconds}s</span>`;
  } else {
    countdownEl.innerHTML = `<span class="countdown-live">The ${eventLabel} has started!</span>`;
  }
}
